package com.projects.studentproject.bean;

public class Student {
    /*Creating a RestApi that returns Student bean as JSON
    Steps :- Created Bean package with Student class
            then Constructor Student is created , then getter setter methods
     */
        int id;
        String name;
        int grade;

    public Student(int id, String name, int grade) {
        this.id = id;
        this.name = name;
        this.grade = grade;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }
}
